# AND
print(True and True)
print(True and False)
print(False and True)
print(False and False)

# OR
print(True or True)
print(True or False)
print(False or True)
print(False or False)

# XOR
print(True != True)
print(True != False)
print(False != True)
print(False != False)

# NOT
print(not True)
print(not False)
print(not not False)
print(not not True)

# 0 (False), 1 (True)
print(not 0)
print(not 1)

# Bitwise operators (Bit-a-bit)
print(True & False)  # 'AND'
print(False | True)  # 'OR'
print(True ^ False)  # 'XOR'
